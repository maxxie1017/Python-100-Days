# 继承Thread类创建自定义的线程类
class DownloadHanlder(Thread):

    def __init__(self, url):
        super().__init__()
        self.url = url

    def run(self):
        filename = self.url[self.url.rfind('/') + 1:]
        resp = requests.get(self.url)
        # 下方不能用相对路径？
        with open('G:/Python/Python-100-Days/Day01-15/code/Day14/' + filename, 'wb') as f:
            f.write(resp.content)


